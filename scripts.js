const platforms = ["PC", "PS5", "Xbox Series X", "Nintendo Switch", "Phone"];
const colors = ["#FF6B6B", "#4ECDC4", "#9D50BB", "#45B7D1", "#96C93D", "#FAA916"];

function generateGames() {
    const gameGrid = document.querySelector('.game-grid');

    for (let i = 0; i < 96; i++) {
        const price = Math.floor(Math.random() * 3000 + 1499);
        const randomPlatforms = platforms.filter(() => Math.random() > 0.5);
        const description = gameDescriptions[Math.floor(Math.random() * gameDescriptions.length)];

        const gameCard = document.createElement('div');
        gameCard.className = 'game-card';
        gameCard.style.background = colors[Math.floor(Math.random() * colors.length)];

        gameCard.innerHTML = `
      <h3>${gameNames[i]}</h3>
      <p>${description}</p>
      <div class="price">${price} ₽</div>
      <div class="platforms">
        ${randomPlatforms.map(p => `<span class="platform-tag">${p}</span>`).join('')}
      </div>
    `;

        gameGrid.appendChild(gameCard);
    }
}

generateGames();